                            <p>We take this opportunity to introduce you to our vision that creates transformative opportunities by connecting art, the city and its people.</p>

                            <p>The journey of the earlier editions of the Karachi Biennale (KB) is available through images and our exhibition catalogues. Through the Biennale theme, we address concerns that are anchored in our city and are also relevant to all large cities, particularly in emerging economies, where the spirit of improvisation and innovation overcomes the lack of resources and infrastructure. At KB, we overcame the absence of a large exhibition space by installing art in heritage sites. This successfully wove the city's history into the Biennale narrative and gave visibility to forgotten spaces. Visitors enjoy these spaces with pride and a sense of discovery.</p>

                            <p>The first Art Biennale was held in Venice in 1895 to promote socio-economic growth and social cohesion. Since then, Biennales have been held in over 200 cities around the world, where they respond to local conditions. In the same spirit of revival and rejuvenation, the Karachi Biennale was created in 2017 by art professionals and educators with the aim of bringing unity and harmony to a city paralysed by violence and social polarization.</p>

                            <p>In the last two decades, art activity in Karachi has increased, making it a vibrant hub. It has been felt, however, that critical knowledge around art needs to keep up. To address this gap, KBT established the Critical Knowledge Lab (CKL) to discuss and document the knowledge that exists in the practice, research, and experiences of artists, social thinkers, and cultural interventionists of our time. To trace how ideas evolve and converge across disciplines, CKL has held roundtables and seminars regularly since 2017.</p>

                            <p>The KB Online Karachi Art Directory offers comprehensive information on the Karachi art scene (including smaller art centres of Sindh like Hyderabad, Jamshoro, Sukkur, and Mirpur Khas). This includes contemporary artists, traditional art exponents, art institutions, and galleries, and has become an important source of information.</p>

                            <p>As an art platform, KBT prides itself on playing a pivotal role in developing Karachi's art infrastructure by creating a support system that embraces capacity building, global exposure, critical dialogue, prestigious art prizes, and residencies for artists and art professionals. The footfall of hundreds of thousands at the KB venues has been a big step in the direction of public engagement with art. The sustainable links with the corporate sector and philanthropists, and partnerships with culture centres both local and international, offer the promise of enduring support to art in the public space.</p>

                            <p>Above all, building a public audience with exposure to museum-quality art from around the world has been a part of the vision of the founders—a vision that has been realized in the last three Biennales.</p>

                            <p>Art holds tremendous power to stimulate reflection and critical thinking by appealing to human emotions and senses. To make the Biennale experience more meaningful, we select themes that are current and relevant. In the aftermath of pandemic lockdowns that brought our city to a standstill, the role of digital technology in personal and professional lives became crucial. The reality of the "new normal" prompted us to explore the intersection of art and technology. Technology as a medium of art was presented at KB22 under the rubric "Collective Imagination: Now and the Next" through the lens of the curator, Faisal Anwar.</p>

                            <p>Conversations and engagements on the creative possibilities of technology were started by the KB22 teams 12 months prior to the Biennale, for which there was an enthusiastic response. Educators and students were eager to experience cutting-edge art from Pakistan and 13 countries.</p>

