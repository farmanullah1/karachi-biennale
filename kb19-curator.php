                <div class="kb19u-statement-body">
                    <p>Where there is life there is also the unmitigated acceptance of codependence, affect and affected change. 'Flight Interrupted: Eco-leaks from the Invasion Desk' encompasses a leitmotif at the heart of Karachi. Using the nuance of her volatile and ephemeral ecology, her challenges are unpacked and used as impetus to imagine and to contextualise memory, binding the past, present and future together.</p>
                    <p>Investigating inherited cultural layers and reconciling the historic with the contemporary, environment becomes the basis of our visual interpretation. Human nature contains a dichotomy: being reliant on, and fearing the natural world. To reconcile with this conflict, humans have attempted to sanitise nature. The curatorial lens is interested in going beyond the rudimentary, emphasising the role of ecology in the choreography of daily life.</p>
                    <p>KB19 looks to take these concerns, Karachi's and the world's, into the space that the public already occupies. A particularly exciting audience, Karachi's population will face these galling concerns in their native city through unexpected, unconventional mediums and venues. KB19 looks to channel the concerns of Karachi's population, presenting them with the unfamiliar, and calls on artists to provoke this discourse, challenging canon, convention and aesthetics.</p>
