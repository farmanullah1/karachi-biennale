                        <p>Anwar is the founder of several digital initiatives, including the interactive art project <em>'I am Karachi'</em> and the digital lab <em>'Tree of Data'</em>. He is a graduate of the National College of Arts (NCA) in Lahore, Pakistan, and holds a Master's degree from OCAD University in Toronto, Canada. Over the past two decades, his work has been exhibited at major museums, public galleries, and art festivals across North America, Europe, and Asia.</p>
                        
                        <p>As a curator, Anwar advocates for democratizing digital art, moving it away from elite white-cube spaces and bringing it directly to public streets, heritage buildings, and schools. His approach to KB22 focused on bridging the digital divide, ensuring that children, families, and everyday citizens of Karachi could interact with, learn from, and shape the technological art on display.</p>
