// Simple dynamic lightbox popup for images if Magnific Popup is not initialized
document.addEventListener('DOMContentLoaded', () => {
    // If jquery and magnificPopup exist, bind them
    if (typeof jQuery !== 'undefined' && jQuery.fn.magnificPopup) {
        jQuery('.kb24w-gallery-grid').magnificPopup({
            delegate: 'a',
            type: 'image',
            gallery: {
                enabled: true
            }
        });
    } else {
        // Fallback vanilla lightbox
        const links = document.querySelectorAll('.kb24w-gallery-link');
        links.forEach(link => {
            link.addEventListener('click', (e) => {
                e.preventDefault();
                const src = link.getAttribute('href');
                const title = link.getAttribute('title') || '';
                
                const box = document.createElement('div');
                box.style.position = 'fixed';
                box.style.inset = '0';
                box.style.backgroundColor = 'rgba(0,0,0,0.9)';
                box.style.zIndex = '100000';
                box.style.display = 'flex';
                box.style.flexDirection = 'column';
                box.style.alignItems = 'center';
                box.style.justifyContent = 'center';
                box.style.cursor = 'pointer';
                
                const img = document.createElement('img');
                img.src = src;
                img.style.maxWidth = '90%';
                img.style.maxHeight = '80vh';
                img.style.borderRadius = '8px';
                img.style.boxShadow = '0 10px 30px rgba(0,0,0,0.5)';
                
                const caption = document.createElement('p');
                caption.textContent = title;
                caption.style.color = '#fff';
                caption.style.marginTop = '15px';
                caption.style.fontSize = '16px';
                caption.style.fontWeight = '600';
                
                box.appendChild(img);
                box.appendChild(caption);
                
                box.addEventListener('click', () => {
                    document.body.removeChild(box);
                });
                
                document.body.appendChild(box);
            });
        });
    }
});
</script>

