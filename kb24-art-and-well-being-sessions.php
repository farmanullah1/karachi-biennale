                    <p class="kb24w-lead">The KB24 GEMAH Wellness Workshop for creative adults was held on November 8–9, 2024, at the NJV School Auditorium in Karachi. This transformative two-day event successfully blended art and emotional well-being to empower young creatives.</p>
                    
                    <p class="kb24w-paragraph">Organized by Giving Education Meaning (GEM) Ltd. in collaboration with Karachi Biennale Trust, this third iteration of the free workshop brought together 60 participants aged 18–30 from diverse creative fields including architecture, filmmaking, photography, poetry, and teaching. The workshop aimed to equip attendees with emotional management tools, foster collaboration, and encourage creative exploration through art.</p>

                    <h3 class="kb24w-section-title">Origins and Evolution</h3>
                    <p class="kb24w-paragraph">The workshop series began in 2019 when artist Shehzar Abro partnered with GEM's Founding Director, Dr. Angé Weinrabe, and Atteqa Malik, Global Outreach Chair of the Karachi Biennale Trust, to address the emotional challenges faced by creatives in Pakistan. Since then, the program has grown to become a cornerstone of KB's community engagement initiatives.</p>

                    <h3 class="kb24w-section-title">Day One: Foundations of Emotional Resilience</h3>
                    <p class="kb24w-paragraph">The first day opened with the GEM coaching module delivered by Dr. Weinrabe, focusing on emotional identification and resilience building. Alumni mentors shared personal experiences, offering invaluable insights from their own creative journeys. Clinical psychologist Dr. Faris Mughal discussed mental health awareness and offered individual counseling sessions, providing participants with practical tools for emotional well-being.</p>

                    <h3 class="kb24w-section-title">Day Two: Creativity and Collaboration</h3>
                    <p class="kb24w-paragraph">Day two featured an online talk by philosopher and musician Dr. Tom Cochrane, who explored the profound link between love and happiness, sparking lively discussions among participants. Aligning with KB24's theme, <strong>Rizq | Risk</strong>, the day also included a collaborative art session where participants created a large-scale portrait of pioneering scientist Professor Rabia Hussain.</p>
                    
                    <p class="kb24w-paragraph">Using lentils and seeds, participants crafted individual 2×2 foot squares, culminating in a breathtaking 15×15 foot mural that highlighted the power of artistic collaboration.</p>

                    <h3 class="kb24w-section-title">Impact and Legacy</h3>
                    <p class="kb24w-paragraph">The KB24 GEMAH Wellness Workshop demonstrated profound impact, providing participants with tools for emotional resilience and creative expression. Many attendees reported feeling more equipped to navigate the challenges of creative work and more connected to Karachi's vibrant artistic community.</p>
