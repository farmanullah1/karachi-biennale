                        <blockquote class="kb19a-pullquote" aria-label="Thematic Opening Statement">
                            <p>"KB19 invites artists to challenge mythologies of development cast in concrete, measured in extinct species, wasted bodies, sterile lands and poisoned waters."</p>
                        </blockquote>
                        
                        <p class="kb19a-paragraph lead">
                            <span class="kb19a-dropcap">A</span> loss, a towering barrier, a biorhythm interrupted. The title invokes the loss of native species of low-flying birds, wiped out by vertical structures in Karachi that broke their free flight and marginalised life-cycles. Living in this city, one is silently complicit to the mercurial landscape.
                        </p>
                        
                        <p class="kb19a-paragraph">
                            'Flight Interrupted: Eco-leaks from the Invasion Desk' underscores the annihilation inflicted on ecosystems by man's war of ambition with nature, and the myriad detrimental reactions from nature. The thematic of KB19 takes the ongoing conversations a notch higher to match the urgency of the onslaught on natural habitats and natural resources. This extremity must be treated as an impetus to cause a reaction, a comment, an alternative to a civilised, established model—an archetype that is considered by a great diversity of stakeholders as a system in crisis.
                        </p>
                        
                        <p class="kb19a-paragraph">
                            Karachi is an exciting, double-sided form: reliable in her recurrence, but open-ended in her actualisation. Approaching her from the sea, the coast—lungs of the city that were once lined with mangroves—is now a harbinger of habitat loss and ecological disaster. Moving inward, land that miraculously supported life on the edge of a desert has been stripped and transformed into a new sort of jungle: one of tar and concrete, ruled not by the laws of nature, but by the toxins of industrial might and greed-based urban planning. Even the sustenance for life, water, is as scarce as it is polluted. Topography altered, a man displaced.
                        </p>
                        
                        <div class="kb19a-gibran-quote-card">
                            <h3 class="kb19a-gibran-title">Reflection by Khalil Gibran</h3>
                            <blockquote class="kb19a-gibran-quote">
                                <p>"Nature reaches out to us with welcoming arms, and bids us enjoy her beauty; but we dread her silence and rush into the crowded cities, there to huddle like sheep fleeing from a ferocious wolf… We are swept into the current of urban existence until we forget the peaceful rhythms of simple country life, which smiles in the spring, toils in the summer, reaps in autumn, rests in winter, imitating nature in all her cycles. We are wealthier than the villagers in silver or gold, but they are richer in spirit. What we sow we reap not; they reap what they sow. We are slaves of gain, and they the children of contentment. Our draught from the cup of life is mixed with bitterness and despair, fear and weariness; but they drink the pure nectar of life's fulfilment."</p>
                            </blockquote>
                        </div>
                        
                        <p class="kb19a-paragraph text-muted mt-4">
                            Most analyses of the situation focus on the economic, ecological and social dimensions of this crisis. KB19 invites artists to explore these avenues but also unpack the cultural dimension of the contemporary crisis of unsustainability, as well as its cultural roots and the origins of this narrative. It offers, in one place, a display of the contemporary art of the world in ways that are entertaining, instructive, and competitive, all at the same time; a constructed eco-archive of personal and national memory, a will to heal and rejuvenate, with strategies to realign with the timeless cycles of nature that pre-date man; a mandate to reveal the diversity, specificity and continuity of art and ecology on the people, place, time and history of this flight.
                        </p>
