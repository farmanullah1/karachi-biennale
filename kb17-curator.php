            <div class="kb17u-statement-card">
                <h3 class="kb17u-section-title"><i class="fas fa-quote-left me-2"></i>Curatorial Statement</h3>
                <div class="kb17u-statement-body">
                    <p>Karachi, where I was born and raised, bore witness to the bloodshed of the partition of the subcontinent into the modern nation states of India and Pakistan in 1947. Despite continuing episodes of turmoil, the city of migrants resiliently grows and thrives. This is especially true in the arts. In 1971, Ali Imam opened the Indus Gallery and it became a focal point for many of our Modernists. Today the gallery scene flourishes and contemporary artists come from all over the country to display their work here. Commercial galleries, however, are not enough. More museums and public spaces to show art are needed in Karachi. It is imperative to establish a biennale in Pakistan’s largest city to engage not only international art audiences but also the people of this diverse metropolis.</p>
                    <p>It is an honor for me to be selected by the KB17 Trust as Chief Curator for this event dedicated to bringing you art every two years. My curatorial team consists of four dynamic individuals who, like me, not only call this maddening, inspiring city home, but have their own art practices. Zeerak Ahmed, Humayun Memon, Sara Paganwala, and Syed Ammad Tahir bring a young perspective and energy to our endeavor.</p>
                    <p>When confronted with the theme for KB17 — Witness — the much-quoted Chinese curse “May you live in interesting times” comes to mind. These are indisputably interesting times not only for my city but also for the world around us. We are often told as artists that our duty is to question the times we live in. If so, even this seemingly familiar “Chinese” proverb demands scrutiny. Fred R. Shapiro, editor of the Yale Book of Quotations, noted, “No authentic Chinese saying to this effect has ever been found.” The British politician Sir Austen Chamberlain first conjured up this expression in a speech in 1936 as reported by the Yorkshire Post.</p>
                    <p>As curator it has been an experiential venture for me to approach artists from both Pakistan and abroad to bear witness to this perplexing era. This has been an opportunity to learn and absorb very diverse artists’ idiosyncratic vocabularies from several continents. Visual artists are not the only ones invited into this discourse; architects, filmmakers, photographers, and professionals engaged with fashion and theater are also included. This cross-disciplinary approach reflects the ethos of Karachi in which there is a great deal of interaction and collaboration among creative communities.</p>
                    <p>The works commissioned and selected for the biennale are both political and personal. The issues addressed by artists based whether here or abroad have a resonance for my city. Some are a commentary on the times and others explore the artists’ own inner dialogues. They are acts of defiance and celebration that will take viewers to places unexpected and unexplainable. The aim is to ponder not only our times, but also the narratives surrounding them. I do not have answers, only questions.</p>
                    <p>“There must be something sacred about salt. It is in our tears and in our sea,” stated Kahlil Gibran. Now, I feel, is the time for us to join together as artists and more importantly as human beings to bear witness to our shared salt.</p>
                </div>
