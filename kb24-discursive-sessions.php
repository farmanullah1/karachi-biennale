                    <p class="kb24d-lead">The Fourth Karachi Biennale hosted the KB24 Discursive Weekend on November 2–3, 2024, at the Sambara Art Gallery Auditorium. This well-attended event featured talks, workshops, and presentations that explored art as a powerful tool for examining food justice, history, and archiving.</p>

                    <h3 class="kb24d-section-title">Day One: Colonial History and Artistic Resistance</h3>
                    <p class="kb24d-paragraph">Dr. Emilia Terraciano (UK) presented her groundbreaking research on the visual archives of the Bengal Famine of 1943—a man-made catastrophe caused by British colonial policies. Termed the "Forgotten Holocaust," her presentation underscored the intersection of art, history, and colonial politics, revealing how archival images can bear witness to systemic injustice.</p>
                    <p class="kb24d-paragraph">Later in the evening, a multilingual <em>mushaira</em> on the theme <strong>Rizq | Risk</strong> took place. The poems underlined the shared concerns between all creatives, regardless of language or origin. Leading poets including Afzal Ahmed Syed, Attiya Dawood, and Sadia Baloch recited their verses in Urdu, Sindhi, Pashto, English, and Brushiski, creating a tapestry of linguistic and cultural expression.</p>

                    <h3 class="kb24d-section-title">Day Two: Decolonization Through Archiving</h3>
                    <p class="kb24d-paragraph">The second day began with an online talk by Dr. Kylie Gilchrist, highlighting Rasheed Araeen's exploration of food in his visionary art career. Her presentation connected Araeen's pioneering work to contemporary conversations about food justice and artistic practice.</p>
                    <p class="kb24d-paragraph">The second session was dedicated to archiving as a tool for the decolonization of knowledge. Tazeen Hussain, Hira Azmat, Veera Rustomji, and Mahnoor Jalal presented their research on the endangered archives of four influential Pakistani publications—the feminist publishing house Simurgh and other journals/magazines including <em>She</em>, <em>NuktaArt</em>, and <em>Paper Magazine</em>. This four-month research project was an initiative of the Pakistan Association of Women Publishers and Editors (PAWPE) in partnership with KB24, Aleph Review, and Teeside University, UK.</p>

                    <h3 class="kb24d-section-title">Archiving Workshop and Panel Discussion</h3>
                    <p class="kb24d-paragraph">The Discursive Weekend concluded with an archiving workshop led by Dr. Madeleine Clements and a panel discussion featuring Tooba Masood Khan on her upcoming book <em>Society Girl</em>, co-authored with Saba Imtiaz. The discussion highlighted how archival investigation is crucial to the book based on the murder case of poet Mustafa Zaidi in Karachi.</p>

                    <h3 class="kb24d-section-title">Artists Talk and Panel Discussion</h3>
                    <p class="kb24d-paragraph">On October 27, 2024, a curator-led panel discussion with participating artists was held at Sambara Art Gallery Auditorium. Chief Curator Waheeda Baloch invited KB24 artists to speak on their work and how it explores the Biennale theme through the lens of environmental challenges, colonial histories, and indigenous knowledge. Artists also shared their creative processes with the audience, offering rare insights into their practices.</p>
