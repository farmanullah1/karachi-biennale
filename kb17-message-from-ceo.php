                            <p>KB17 is Pakistan’s largest international contemporary art event that will take place every two years in Karachi. This October, over 140 artists from Pakistan and across the globe will respond to a common theme: <strong>WITNESS</strong>.</p>
                            
                            <p>Unbounded by a canon, Karachi is unique for her persistently altering memory. A maritime metropolis that embraces independent thinkers like the Modernist Masters of the 1950s, outfitted with three major art schools, and site of South Asia’s most dynamic art gallery circuits, Karachi is among the Art world’s best-kept secrets.</p>
                            
                            <p>We invite you to witness our city – over 22 million strong and sprawling – through the lens of art. Hosted at 12 diverse venues including schools, heritage sites, cinemas, and public parks, the Biennale will draw art out of the gallery and into the spaces that express Karachi’s quotidian pulse. The main venue of KB17 is the 160-year-old, NJV School building.</p>
                            
                            <p>KB17’s two-week-long, free public exhibitions will feature performances, screenings, and dialogue for charting new movement through familiar spaces.</p>
                            
                            <p>KB17 has chosen to locate its principal venue at the 160-year-old NJV School, a building that housed the first National Assembly of Pakistan to invite reflection on the city’s history that has always embraced diversity and intellectual independence. This site on M.A. Jinnah Road, the oldest artery, will once again be a link, this time between visitors experiencing art for the first time and seasoned art enthusiasts. To engage them all at multiple levels, an extensive visitor program is planned with artists’ talks, keynote speakers, and exhibition tours.</p>
                            
                            <p>The First KB17 is honored to be a part of Pakistan’s 70-year celebrations, and to introduce its vision, to showcase artistic excellence, and to create a wider awareness of the transformative energy of Art.</p>
